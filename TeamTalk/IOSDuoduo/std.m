char getFirstChar(const NSString * str) {
	if (nil == str || 0 == [str length]) {
		return '#';
	}
	const char * firstChar = [str UTF8String];
	if ( ('a'<=  *firstChar && *firstChar <= 'z') 
		|| ('A' <= *firstChar && *firstChar <= 'Z')) {
		return *firstChar;
	}
	else {
		return pinyinFirstLetter([str characterAtIndex:0]);
	}
}


